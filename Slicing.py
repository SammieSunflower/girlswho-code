print("Slicing Lists")
a = [1, 2, 3, 4, 5, 6, 7, 8, 9]
print("We have a list of the numbers 1 thorugh 9")
print(a)
b = a[1:4]
print(b)
#by doing a[1:4] to our list it will display
c = a[1:4:2]
print(c)
d = a[::-1]
print(d)

